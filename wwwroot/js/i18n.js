// ── Core engine ─────────────────────────────────────────────

const I18n = (() => {
  const STORAGE_KEY = "saigonride_lang";
  let currentLang = localStorage.getItem(STORAGE_KEY) || "en";

  function t(key) {
    return (translations[currentLang] && translations[currentLang][key]) ||
           (translations["en"][key]) ||
           key;
  }

  function applyTranslations() {
    // data-i18n="key" → sets textContent
    document.querySelectorAll("[data-i18n]").forEach(el => {
      const key = el.getAttribute("data-i18n");
      el.textContent = t(key);
    });

    // data-i18n-html="key" → sets innerHTML (for <strong> etc.)
    document.querySelectorAll("[data-i18n-html]").forEach(el => {
      const key = el.getAttribute("data-i18n-html");
      el.innerHTML = t(key);
    });

    // data-i18n-placeholder="key" → sets placeholder
    document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
      const key = el.getAttribute("data-i18n-placeholder");
      el.placeholder = t(key);
    });

    // Update toggle button label
    const btn = document.getElementById("lang-toggle-btn");
    if (btn) {
      btn.innerHTML = currentLang === "en"
        ? '<span class="lang-flag">🇻🇳</span><span class="lang-label"> Tiếng Việt</span>'
        : '<span class="lang-flag">🇬🇧</span><span class="lang-label"> English</span>';
    }

    // Update <html lang>
    document.documentElement.lang = currentLang === "vi" ? "vi" : "en";
  }

  function toggle() {
    currentLang = currentLang === "en" ? "vi" : "en";
    localStorage.setItem(STORAGE_KEY, currentLang);
    applyTranslations();
  }

  function init() {
    applyTranslations();
    const btn = document.getElementById("lang-toggle-btn");
    if (btn) btn.addEventListener("click", toggle);
  }

  return { init, toggle, t, get lang() { return currentLang; } };
})();

document.addEventListener("DOMContentLoaded", I18n.init);
